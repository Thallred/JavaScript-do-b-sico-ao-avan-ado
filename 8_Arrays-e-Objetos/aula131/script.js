// Lembrando que o primeiro índice sempre é o 0, ou seja, para acessar o primeiro elemento: arr[0];

let arr = [1, 2, 3, 4, 5, 6, 7];

let nomes = ['Fernando', 'Filomena', 'João', 'Thais'];

let bool = [true, false, true, false];


let misturado = [1, 'Fernando', true];


console.log(arr[1]); // Acesssando o segundo elemento

console.log(nomes[0]); // Acessando o primeiro elemento 

