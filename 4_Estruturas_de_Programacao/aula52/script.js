// Função utilizada para expressões/cálculos matemáticos

// É muito utilizada

let maior = Math.max(6,12,28,55,8); // Math.max irá verificar qual é o maior número

console.log(maior);

let menor = Math.min(6,12,28,55,8); // Math.min irá verificar qual é o menor número

console.log(menor);

let arredondar = Math.round(5.298654321); // Math.round irá arredondar um número decimal, para cima ou para baixo, de acordo com as casas decimais

console.log(arredondar);

let arredondarParaCima = Math.ceil(5.203938474); // Math.ceil irá arredondar para cima independente das casas decimais serem baixas ou altas

console.log(arredondarParaCima);