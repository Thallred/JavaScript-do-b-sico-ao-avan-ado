// Escreva uma função que receba dois números, o primeiro é a base e o segundo a potência

// Depois faça essa operação e retorne o resultado

// Por exemplo: 3, 2 = 9

function elevarAPotencia(base, potencia) {
    return Math.pow(base,potencia);
}

console.log(elevarAPotencia(3,2));

console.log(elevarAPotencia(5,3));

console.log(elevarAPotencia(4,2));