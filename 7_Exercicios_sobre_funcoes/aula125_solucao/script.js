function potencia(a,b) {
    return Math.pow(a,b);

}

console.log(potencia(2,2));

console.log(potencia(3,2));

console.log(potencia(4,4));



// Outra forma de resolver 
function potencia2(a,b) {
    return a ** b;

}

console.log(potencia2(2,2));

console.log(potencia2(3,2));

console.log(potencia2(4,4));
